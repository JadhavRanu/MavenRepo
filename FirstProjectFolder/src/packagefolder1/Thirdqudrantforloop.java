public class Thirdqudrantforloop {
	public static void rightqudrant() {
	
		
	for(int k=1;k<=7;k++) 
	{
		for(int l=1;l<k;l++) 
		{
			System.out.print(" ");
		}
			for(int m=7;m>=k;m--) 
			{
			System.out.print("*");
			} 
			System.out.println();		
		
		
	}	}
		
	public static void main(String args[]) 
	{	
		rightqudrant();
		
	}}


